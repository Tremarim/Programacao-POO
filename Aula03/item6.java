package Aula03;

public class item6 {

	public static void main(String[] args) {
		System.out.println("Repetir numeros");
		System.out.println();
		for (int i=1; i<=100 ;i++) {
			System.out.println("Numero: " + i);
		}
		int i=1;
		while (i<=100) {
			System.out.println("Numero: " +i);
			i++;
		}
	}

}
